package basics;

public class CallStaticMethod {

	public static void main(String[] args) {
		
		System.out.println("Teacher Name : Manish");
		Student();
		Student1();
		Student2();
		
	}
	static void Student()
	{
		System.out.println("Student name : Neha");
	}
    static void Student1()
    {
    	System.out.println("Student1 name : Aniket");
    	
    }
    static void Student2()
    {
    	System.out.println("Student2 name : Akshad");

    }
}
